// Value Type Variable

let fruit = 'banana';

let newFruit = fruit;

console.log(fruit);
console.log(newFruit);

newFruit = 'apple';
console.log(newFruit);
console.log(fruit);

let nums = [23, 44, 56];
let otherNums = nums;

console.log(nums);
console.log(otherNums);

otherNums.push(77);

console.log(nums);
console.log(otherNums);
